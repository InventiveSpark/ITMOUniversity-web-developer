       внутри класса и снаружи. Это видимость по умолчанию для всех членов
       класса в PHP.
       Конструктор выполняется автоматически при создании нового объекта
       на основе класса Controller: */

    function __construct()
    {
        // Создание экземпляра объекта View:
        $this->view = new View();
    }

    /* Это общий метод. Он будет конкретизироваться при реализации моделей
       отдельных страниц: */
    function action_index()
    {
    }
}
?>
