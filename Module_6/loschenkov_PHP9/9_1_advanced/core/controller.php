<?php
/* Описание класса Controller - общего класса для контроллера. Он умеет при
   инициализации создавать экземпляр объекта View.
   Определение класса: */
class Controller
{
    // Объявление свойств контроллера:
    public $model; // модель
    public $view;  // представление
    /* Ключевое слово public перед методом или свойством - модификатор доступа,
